class Main {
  public static void main(String[] args) {
    /**
     * Math methods:
     * 1. Math.max()
     * 2. Math.min()
     * 3. Math.sqrt()
     * 4. Math.abs()
     * 5. Math.random()
     */
    
     // getting max value
     System.out.println(Math.max(2, 10));

     // getting min value
     System.out.println(Math.min(2, 10));

     // getting square root of a value
     System.out.println(Math.sqrt(256));

     // getting absolute value
     System.out.println(Math.abs(-256));

     // getting random value from 50 to 100
     System.out.println((Math.random() * (100 - 50)) + 50 + 1);
  }  
}
